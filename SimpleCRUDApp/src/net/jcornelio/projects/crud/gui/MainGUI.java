    private void menuOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuOpenActionPerformed
        chooser = new JFileChooser(); 
        chooser.setDialogTitle("Open File");
        chooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        WindowHelper.setStartUpLocationToCenter(chooser);
        chooser.setAcceptAllFileFilterUsed(false);
        
        if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) { 
          System.out.println("getCurrentDirectory(): " 
             +  chooser.getCurrentDirectory());
          System.out.println("getSelectedFile() : " 
             +  chooser.getSelectedFile());
            FileManager.getInstance().setFile(chooser.getSelectedFile());
            csv.populateListOfValuesFromCSV(chooser.getSelectedFile(), tableOfValues);
            TableController.getInstance().populateTable(ContactsHolder.getInstance().getContacts());
          }
        else {
          System.out.println("No Selection ");
        }
    }//GEN-LAST:event_menuOpenActionPerformed

