    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = TableController.getInstance().getTable().getSelectedRow();
        String delContact = TableController.getInstance().getTable().getValueAt(selectedRow, 0).toString();
        ContactsHolder.getInstance().deleteContact(delContact);
        TableController.getInstance().refreshTable(ContactsHolder.getInstance().getContacts());
    }//GEN-LAST:event_btnDeleteActionPerformed

